func (mpc *MPC) EBitsADD(a, b []*paillier.Ciphertext) []*paillier.Ciphertext {

	if len(a) < len(b) {
		a = mpc.makeEqualLength(a, b)
	} else {
		b = mpc.makeEqualLength(b, a)
	}

	degree := len(a)
	carries := mpc.EBitsCarries(a, b)

	sum := make([]*paillier.Ciphertext, degree+1)
	lsb := mpc.Pk.EAdd(a[0], b[0])
	lsb = mpc.Pk.ESub(lsb, mpc.Pk.ECMult(carries[0], big.NewInt(2)))
	sum[0] = lsb
	sum[degree] = carries[degree-1]

	for i := 1; i < degree; i++ {
		sum[i] = mpc.Pk.EAdd(a[i], b[i])
		sum[i] = mpc.Pk.EAdd(sum[i], carries[i-1])
		sum[i] = mpc.Pk.ESub(sum[i], mpc.Pk.ECMult(carries[i], big.NewInt(2)))
	}

	return sum
}

func (mpc *MPC) EBitsLessThan(a, b []*paillier.Ciphertext) *paillier.Ciphertext {

	//fmt.Println("[DEBUG]:  EBitsLessThan()")

	if len(a) < len(b) {
		a = mpc.makeEqualLength(a, b)
	} else {
		b = mpc.makeEqualLength(b, a)
	}

	degree := len(a) // len(a) = len(b) now
	e := make([]*paillier.Ciphertext, degree)

	for i := 0; i < degree; i++ {
		d := mpc.Pk.ESub(a[i], b[i])
		d2 := mpc.EMult(d, d)
		e[degree-i-1] = d2
	}

	f := mpc.EBitsPrefixOR(e)

	g := make([]*paillier.Ciphertext, degree)
	g[0] = f[0]
	for i := degree - 1; i > 0; i-- {
		g[i] = mpc.Pk.ESub(f[i], f[i-1])
	}

	h := make([]*paillier.Ciphertext, degree)
	for i := 0; i < degree; i++ {
		h[i] = mpc.EMult(g[degree-i-1], b[i])
	}

	res := mpc.Pk.EncryptInt(big.NewInt(0))

	for i := 0; i < degree; i++ {
		res = mpc.Pk.EAdd(res, h[i])
	}

	return res
}

func (mpc *MPC) EBitsCarries(a, b []*paillier.Ciphertext) []*paillier.Ciphertext {

	//fmt.Println("[DEBUG]:  EBitsCarries()")

	if len(a) < len(b) {
		a = mpc.makeEqualLength(a, b)
	} else {
		b = mpc.makeEqualLength(b, a)
	}

	one := mpc.Pk.EncryptInt(big.NewInt(1))
	degree := len(a) // len(a) = len(b) now

	s := make([]*paillier.Ciphertext, degree)
	p := make([]*paillier.Ciphertext, degree)
	k := make([]*paillier.Ciphertext, degree)
	spks := make([]*spk, degree)

	for i := 0; i < degree; i++ {
		mul := mpc.EMult(a[i], b[i])
		s[i] = mul
	}

	for i := 0; i < degree; i++ {
		// compute propagate bit
		d := mpc.Pk.EAdd(a[i], b[i])
		p[i] = mpc.Pk.ESub(d, mpc.Pk.ECMult(s[i], big.NewInt(2)))

		// compute kill bit
		d = mpc.Pk.EAdd(s[i], p[i])
		k[i] = mpc.Pk.ESub(one, d)

		spks[i] = &spk{s: s[i], p: p[i], k: k[i]}
	}

	f := mpc.EBitsPrefixSPK(spks)

	res := make([]*paillier.Ciphertext, degree)

	for i := 0; i < degree; i++ {
		res[i] = f[i].s
	}

	return res
}

func (mpc *MPC) makeEqualLength(a, b []*paillier.Ciphertext) []*paillier.Ciphertext {
	zero := mpc.Pk.EncryptInt(big.NewInt(0))
	delta := len(b) - len(a)
	zeroArray := make([]*paillier.Ciphertext, delta)
	for i := 0; i < delta; i++ {
		zeroArray[i] = zero
	}

	return append(a, zeroArray...)
}

func (mpc *MPC) bitsSPK(tups []*spk) *spk {

	//fmt.Println("[DEBUG]:  bitsSPK()")

	size := len(tups)

	b := tups[0].p
	for i := 1; i < size; i++ {
		b = mpc.EMult(b, tups[i].p) // equiv to AND operation
	}

	allPs := make([]*paillier.Ciphertext, size)
	for i := 0; i < size; i++ {
		allPs[i] = tups[size-i-1].p
	}

	preAnd := mpc.ReverseBits(mpc.EFanInMULT(allPs))

	carries := make([]*paillier.Ciphertext, size)
	carries[size-1] = tups[size-1].k

	var wg sync.WaitGroup
	wg.Add(size - 1)
	for i := 0; i < size-1; i++ {
		go func(i int) {
			defer wg.Done()
			mul := mpc.EMult(tups[i].k, preAnd[i+1]) // equiv to AND operation
			carries[i] = mul
		}(i)
	}

	wg.Wait()

	zero := mpc.Pk.EncryptInt(big.NewInt(0))
	one := mpc.Pk.EncryptInt(big.NewInt(1))

	sum := zero
	for i := 0; i < size; i++ {
		sum = mpc.Pk.EAdd(sum, carries[i])
	}

	diff := mpc.Pk.EAdd(b, sum)
	a := mpc.Pk.ESub(one, diff)

	return &spk{s: a, p: b, k: sum}
}

func (mpc *MPC) ReverseBits(bits []*paillier.Ciphertext) []*paillier.Ciphertext {

	size := len(bits)
	bitsR := make([]*paillier.Ciphertext, size)
	for i := 0; i < size; i++ {
		bitsR[size-i-1] = bits[i]
	}

	return bitsR
}

// BitsBigEndian returns the n-bit representation of an integer a
func (mpc *MPC) BitsBigEndian(a *big.Int, n int) []*big.Int {

	bitsMutex.Lock()
	s := fmt.Sprintf("%b", a)
	bits := make([]*big.Int, len(s))
	k := 0

	for i := len(s) - 1; i >= 0; i-- {
		bits[k] = big.NewInt(int64(s[i] - '0'))
		k++
	}

	for i := n - len(s); i >= 0; i-- {
		bits = append(bits, big.NewInt(0))
	}

	bitsMutex.Unlock()

	return bits
}

// EBitsBigEndian returns the n-bit (encrypted) representation of an integer a
func (mpc *MPC) EBitsBigEndian(a *big.Int, n int) []*paillier.Ciphertext {

	bitsMutex.Lock()

	s := fmt.Sprintf("%b", a)
	bits := make([]*paillier.Ciphertext, len(s))
	k := 0
	for i := len(s) - 1; i >= 0; i-- {
		bits[k] = mpc.Pk.EncryptInt(big.NewInt(int64(s[i] - '0')))
		k++
	}

	zero := mpc.Pk.EncryptInt(big.NewInt(0))
	for i := n - len(s) - 1; i >= 0; i-- {
		bits = append(bits, zero)
	}

	bitsMutex.Unlock()

	return bits
}

// EBitsZero returns the n-bit vector of zeros
func (mpc *MPC) EBitsZero() []*paillier.Ciphertext {

	n := mpc.Pk.T.BitLen()
	bits := make([]*paillier.Ciphertext, n)
	zero := mpc.Pk.EncryptInt(big.NewInt(0))

	for i := 0; i < n; i++ {
		bits[i] = zero
	}

	return bits
}

func neg(a *big.Int, p *big.Int) *big.Int {
	return big.NewInt(0).Sub(p, a)
}

func (mpc *MPC) computeLangragePolynomialCache(n int) {

	for i := 0; i < n; i++ {
		binaryORFunctionInterpolation(i, mpc.Pk.N)
	}
}

// compute the lagrange interpolation of the function such that
// f(1) = 0, f(1) = f(2) = f(3).... = 1
func binaryORFunctionInterpolation(n int, p *big.Int) []*big.Int {

	//fmt.Println("[DEBUG]: binaryORFunctionInterpolation()")

	if value, found := lagrangeCache.Load(n); found {
		if v, ok := value.([]*big.Int); ok {
			out := make([]*big.Int, n+1)
			for i := 0; i <= n; i++ {
				out[i] = big.NewInt(0).Set(v[i])
			}
			return out
		}
	}

	//fmt.Println("[DEBUG]: binaryORFunctionInterpolation()")

	var numerator []*big.Int
	var poly []*big.Int

	denom := big.NewInt(1)
	poly = make([]*big.Int, n)

	for k := 0; k < n; k++ {
		poly[k] = big.NewInt(0)
	}

	for i := 1; i <= n; i++ {
		for j := 0; j <= n; j++ {
			if i != j {
				xi := big.NewInt(int64(i + 1))
				xj := big.NewInt(int64(j + 1))

				if numerator == nil {
					numerator = []*big.Int{big.NewInt(1), neg(xi, p)}
				} else {
					numerator = polyMult(numerator, []*big.Int{big.NewInt(1), neg(xj, p)})
				}

				denom.Mul(denom, big.NewInt(0).Add(xi, neg(xj, p)))
			}
		}

		if big.NewInt(0).Abs(denom).Cmp(big.NewInt(1)) != 0 {
			denom = denom.ModInverse(denom, p)
		}

		for i := 0; i < len(numerator); i++ {
			numerator[i].Mul(numerator[i], denom)
		}

		poly = polyAdd(poly, numerator)

		// reset
		denom = big.NewInt(1)
		numerator = nil

	}

	for i := 0; i < len(poly); i++ {
		if poly[i].Cmp(big.NewInt(0)) < 0 {
			poly[i] = poly[i].Neg(poly[i])
			poly[i] = poly[i].Mod(poly[i], p)
			poly[i] = neg(poly[i], p)
		} else {
			poly[i] = poly[i].Mod(poly[i], p)
		}
	}

	lagrangeCache.Store(n, poly)

	return poly
}

func polyMult(poly1 []*big.Int, poly2 []*big.Int) []*big.Int {
	n := len(poly1) + len(poly2)
	result := make([]*big.Int, n)
	for k := 0; k < n; k++ {
		result[k] = big.NewInt(0)
	}

	for i, coefficient := range poly1 {
		for j, coefficient2 := range poly2 {
			result[i+j].Add(result[i+j], big.NewInt(0).Mul(coefficient, coefficient2))
		}
	}
	return result[0 : n-1]
}

func polyAdd(poly1 []*big.Int, poly2 []*big.Int) []*big.Int {
	len1 := len(poly1)
	len2 := len(poly2)

	result := make([]*big.Int, len1)

	if len1 >= len2 {
		for i := 0; i < len1-len2; i++ {
			result[i] = poly1[i]
		}

		k := 0
		for i := len1 - len2; i < len1; i++ {
			result[i] = big.NewInt(0).Add(poly1[i], poly2[k])
			k++
		}

	} else {
		result = polyAdd(poly2, poly1)
	}

	return result

}
