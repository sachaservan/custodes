func (mpc *MPC) precomputeSolvedBits(n int) {
	solvedBits = make([][]*paillier.Ciphertext, n)
	solvedBitsValue = make([]*paillier.Ciphertext, n)

	var wg sync.WaitGroup
	for i := 0; i < n; i++ {
		wg.Add(1)
		go func(i int) {
			defer wg.Done()
			bits, d, err := mpc.solvedBits(false)
			for err != nil {
				//fmt.Println("[DEBUG]: solvedBits() aborted.")
				bits, d, err = mpc.solvedBits(false)
			}
			solvedBits[i] = bits
			solvedBitsValue[i] = d
			//fmt.Println("Finished generating solved bits")

		}(i)
	}

	wg.Wait()
}

